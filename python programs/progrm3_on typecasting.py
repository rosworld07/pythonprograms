
#program on typecasting 
#for int number
x = int(1)
print(x) # x is  1
y = int(2.8)
print(y)# y is 2
z = int("3")
print(z)# z is 3

#for float number
x = float(1)
print(x)# x is 1.0
y = float(2.8)
print(y)# y is 2.8
z = float("3")
print(z)# z is 3.0
w = float("4.2")
print(w) # w is 4.2

# for strings

x = str("s1")
print(x) # x  is ill be 's1'
y = str(2)
print(y)# y is '2'
z = str(3.0)
print(z)# z is '3.0'
